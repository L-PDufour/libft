#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*temp;

	temp = NULL;
	while (*s != '\0')
	{
		if (*s == c)
		{
			temp = (char *)s;
		}
		s++;
	}
	return (temp);
}
